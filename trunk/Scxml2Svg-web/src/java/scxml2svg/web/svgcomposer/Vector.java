package scxml2svg.web.svgcomposer;

/**
 *
 * @author pepin_000
 */
public class Vector {
    public Vector(double x, double y)
    {
        this.x = x;
        this.y = y;
    }
    
    public double getX()
    {
        return x;
    }
    
    public double getY()
    {
        return x;
    }
    
    private double x, y;
}
